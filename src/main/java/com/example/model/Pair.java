package com.example.model;

public class Pair {
    private int v, wt;

    Pair(int v, int wt) {
        this.v = v;
        this.wt = wt;
    }

    public void setV(int v) {
        this.v = v;
    }

    public int getV() {
        return v;
    }

    public void setWt(int wt) {
        this.wt = wt;
    }

    public int getWt() {
        return wt;
    }
}
